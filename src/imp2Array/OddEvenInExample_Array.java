package imp2Array;

public class OddEvenInExample_Array {
	//Java Program To Print the Odd and Even Numbers in an Array
	
	public static void main(String args[]){  
		
		int a[]={1,2,5,6,3,2};   //Declare an array size.
		
		System.out.println("Odd Numbers:");  
		
		for(int i=0;i<a.length;i++){  // Loop through the array and select an element.
			
			if(a[i]%2!=0){   //Check whether even or not
			
			System.out.println(a[i]);  
		} } 
		System.out.println("Even Numbers:"); 
		
		
		for(int i=0;i<a.length;i++){  //Loop through the array and select an element.
			
			
		if(a[i]%2==0){   //Check whether odd or not
			
		System.out.println(a[i]);  
		
		
		}  }}}
		

